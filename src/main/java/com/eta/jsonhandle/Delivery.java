package com.eta.jsonhandle;

public class Delivery {
	
	
	private String approach ;
	private String packaging;
	private String dateValidty ;
	private String exportPort;
	private String countryOfOrigin;
	private Double grossWeight;
	private Double netWeight;
	private String terms;
	
	public String getApproach() {
		return approach;
	}
	public void setApproach(String approach) {
		this.approach = approach;
	}
	public String getPackaging() {
		return packaging;
	}
	public void setPackaging(String packaging) {
		this.packaging = packaging;
	}
	public String getDateValidty() {
		return dateValidty;
	}
	public void setDateValidty(String dateValidty) {
		this.dateValidty = dateValidty;
	}
	public String getExportPort() {
		return exportPort;
	}
	public void setExportPort(String exportPort) {
		this.exportPort = exportPort;
	}
	public String getCountryOfOrigin() {
		return countryOfOrigin;
	}
	public void setCountryOfOrigin(String countryOfOrigin) {
		this.countryOfOrigin = countryOfOrigin;
	}
	public Double getGrossWeight() {
		return grossWeight;
	}
	public void setGrossWeight(Double grossWeight) {
		this.grossWeight = grossWeight;
	}
	public Double getNetWeight() {
		return netWeight;
	}
	public void setNetWeight(Double netWeight) {
		this.netWeight = netWeight;
	}
	public String getTerms() {
		return terms;
	}
	public void setTerms(String terms) {
		this.terms = terms;
	}

	
}
